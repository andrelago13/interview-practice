    public static String solve(BufferedReader f) throws Exception {
        StringTokenizer st = new StringTokenizer(f.readLine());

        int num = Integer.parseInt(st.nextToken());
        results = new ArrayList<>();

        int quot = num;
        ArrayList<Integer> number = new ArrayList<>();
        while(quot != 0) {
            int rem = quot % 10;
            number.add(number.size(), rem);
            quot = quot/10;
            System.out.print(rem);
        }
        rec("", number);

        String res = "";
        for(int i = 0; i < results.size(); ++i) {
            res += results.get(i);
            if(i < results.size() - 1) {
                res += '\n';
            }
        }
        return res;
    }

    public static void rec(String soFar, ArrayList<Integer> num) {
        int val = num.get(0);
        ArrayList<Integer> rest = new ArrayList<>(num);
        rest.remove(0);

        char[] chars = getChars(val);
        for(char c : chars) {
            if(rest.size() == 0) {
                String temp = soFar + c;
                if(dict.contains(temp)) {
                    results.add(temp);
                }
            } else {
                rec(soFar + c, rest);
            }
        }
    }

